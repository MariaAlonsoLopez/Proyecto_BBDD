CREATE TABLE SOCIO(
    N_SOCIO NUMBER(8) CONSTRAINT SOC_NUM_PK PRIMARY KEY,
    NOMBRE VARCHAR2(20),
    ANTIGUEDAD NUMBER(3)
);
CREATE TABLE ABONADO(
    N_ABONADO NUMBER(8) CONSTRAINT ABO_NUM_PK PRIMARY KEY,
    DNI VARCHAR2(9),
    NOMBRE VARCHAR2(20),
    PARENTESCO VARCHAR2(9),
    EDAD NUMBER(2),
    FECHA_INGRESO DATE,
    N_SOCIO NUMBER(8) CONSTRAINT ABO_SOC_FK REFERENCES SOCIO,
    CONSTRAINT ABO_PAR_CK CHECK (UPPER(PARENTESCO) IN ('PRINCIPAL','HIJO/A','PAREJA','MADRE','PADRE', 'OTRO'))
);

CREATE TABLE PISTA(
    N_PISTA CHAR(3) CONSTRAINT PIS_NUM_PK PRIMARY KEY,
    TIPO VARCHAR2(10),
    PRECIO NUMBER(3,1),
    CONSTRAINT PIS_PRE_CK CHECK (PRECIO>0),
    CONSTRAINT PIS_TIP_CK CHECK (UPPER(TIPO) IN ('BALONCESTO','TENIS','PADDEL','VOLEY','FUTBOL'))
);

CREATE TABLE RESERVA(
    N_SOCIO NUMBER(8) CONSTRAINT RES_SOC_FK REFERENCES SOCIO,
    N_PISTA CHAR(3) CONSTRAINT RES_PIS_FK REFERENCES PISTA,
    DIA NUMBER(2),
    HORA NUMBER(2),
    MES NUMBER(2),
    ANYO NUMBER(4),
    LUZ VARCHAR2(3),
    TIPO_PAGO VARCHAR2(10),
    CONSTRAINT RES_LUZ_CK CHECK (UPPER(LUZ) IN ('CON','SIN')),
    CONSTRAINT RES_CLA_PK PRIMARY KEY (N_SOCIO, N_PISTA, DIA, HORA)
);

CREATE TABLE EMPLEADO(
    N_EMPLEADO NUMBER(8) CONSTRAINT EMP_NUM_PK PRIMARY KEY,
    NOMBRE VARCHAR2(20),
    DNI VARCHAR2(9),
    TELEFONO NUMBER(9),
    PUESTO VARCHAR2(13),
    CONSTRAINT EMP_PUE_CK CHECK (UPPER(PUESTO) IN ('MONITOR','MANTENIMIENTO','RESTAURANTE'))
);

CREATE TABLE MONITOR(
    N_EMPLEADO NUMBER(8) CONSTRAINT MON_EMP_FK REFERENCES EMPLEADO,
    TITULO VARCHAR2(10) CONSTRAINT MON_TIT_NN NOT NULL,
    CONSTRAINT MON_NUM_PK PRIMARY KEY (N_EMPLEADO)
);

CREATE TABLE MANTENIMIENTO(
    N_EMPLEADO NUMBER(8) CONSTRAINT MAN_EMP_FK REFERENCES EMPLEADO,
    ESPECIALIDAD VARCHAR2(20) CONSTRAINT MAN_ESP_CK CHECK (UPPER(ESPECIALIDAD) IN ('ELECTRICIDAD','FONTANERIA','JARDINERIA')),
    CONSTRAINT MAN_NUM_PK PRIMARY KEY (N_EMPLEADO)
);

CREATE TABLE RESTAURANTE(
    N_EMPLEADO NUMBER(8) CONSTRAINT RES_EMP_FK REFERENCES EMPLEADO,
    LABOR VARCHAR2(8) CONSTRAINT RES_LAB_CK CHECK (UPPER(LABOR) IN ('COCINA','SALON','LIMPIEZA')),
    CONSTRAINT RES_NUM_PK PRIMARY KEY (N_EMPLEADO)
);

CREATE TABLE GRUPO(
    NIVEL VARCHAR2(8),
    TIPO VARCHAR2(10),
    MAX_ALUMNOS NUMBER(2),
    N_MONITOR NUMBER(8) CONSTRAINT CLA_MON_FK REFERENCES MONITOR,
    CONSTRAINT GRU_NIV_CK CHECK (UPPER(NIVEL) IN ('INFANTIL','CADETE','JUVENIL','SENIOR')),
    CONSTRAINT GRU_TIP_CK CHECK (UPPER(TIPO) IN ('BALONCESTO','TENIS','PADDEL','VOLEY','FUTBOL')),
    CONSTRAINT GRU_GRU_PK PRIMARY KEY (NIVEL, TIPO)
);

CREATE TABLE CLASE(
    NIVEL VARCHAR2(8),
    TIPO VARCHAR2(10),
    N_ABONADO NUMBER(8) CONSTRAINT CLA_ABO_FK REFERENCES ABONADO,
    CONSTRAINT CLA_GRU_FK FOREIGN KEY (NIVEL, TIPO) REFERENCES GRUPO,
    CONSTRAINT CLA_CLA_PK PRIMARY KEY (NIVEL, TIPO, N_ABONADO)
);

